SELECT PRODUCT_CODE, SUM(SALES_AMOUNT * PRICE) SALES
FROM PRODUCT JOIN OFFLINE_SALE
ON PRODUCT.PRODUCT_ID = OFFLINE_SALE.PRODUCT_ID
GROUP BY PRODUCT_CODE, PRICE
ORDER BY SALES DESC, PRODUCT_CODE ASC